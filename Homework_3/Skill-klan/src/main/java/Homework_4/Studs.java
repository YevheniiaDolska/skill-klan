package Homework_4;

//We create an array and go through it using for cycle

public class Studs {
    public static void main(String[] args) {
        String[] studs = {"Kate", "Jack", "Pablo", "Lisa", "Rachel"};
        for (int i = 0; i < studs.length; i++){
            System.out.println(studs[i]);
        }

    }
}
